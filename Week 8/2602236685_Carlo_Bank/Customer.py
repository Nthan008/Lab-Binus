    def __init__(self,f,l,p):
        self.__fName = f
        self.__lName = l
        self.__pin = p
        
    def getFirstName(self):
        return self.__fName
    
    def getLastName(self):
        return self.__lName
    
    def getAccount(self):
        return self.__account
    
    def setAccount(self,account):
        self.__account = account
        
    def setPin(self,p):
        self.__pin = p
        
        
    
        
    
    
    